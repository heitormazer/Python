#Crie um algoritmo que leia um número e mostre o seu dobro, triplo, e raiz quadrada.
num = int(input('Digite um número: '))
numdob = num * 2
numtri = num * 3
numrai = num ** 0.5
print('O número digitado foi {}, seu dobro é {}, seu triplo {}, e sua raiz quadrada {}.'.format(num,numdob,numtri,numrai))
